#!/usr/bin/env node
/**
 * Runs slow4g, fast4g, none and writes docs/lighthouse/network-profiles/baseline.md
 */

import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { spawn } from "node:child_process";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(__dirname, "..");
const outDir = path.join(REPO_ROOT, "docs", "lighthouse", "network-profiles");

function runNode(scriptArgs) {
  return new Promise((resolve, reject) => {
    const child = spawn(
      process.execPath,
      [path.join(__dirname, "run-lighthouse-network.mjs"), ...scriptArgs],
      {
        cwd: REPO_ROOT,
        env: process.env,
        stdio: ["ignore", "pipe", "inherit"],
      },
    );
    let out = "";
    child.stdout?.on("data", (c) => {
      out += c;
    });
    child.on("error", reject);
    child.on("close", (code) => {
      if (code !== 0) reject(new Error(`child exit ${code}`));
      else resolve(out.trim());
    });
  });
}

for (const p of ["slow4g", "fast4g", "none"]) {
  console.error(`Running Lighthouse (${p})…`);
  await runNode([p]);
}

const slow = JSON.parse(
  await readFile(path.join(outDir, "summary-slow4g.json"), "utf8"),
);
const fast = JSON.parse(
  await readFile(path.join(outDir, "summary-fast4g.json"), "utf8"),
);
const none = JSON.parse(await readFile(path.join(outDir, "summary-none.json"), "utf8"));

const md = `<!-- auto-generated by scripts/run-lighthouse-network-all.mjs — do not edit numbers by hand -->
# Lighthouse 네트워크 프로필 베이스라인 (3환경)

**대상 URL:** \`${slow.url}\`  
**측정:** slow4g ${slow.fetchedAt} / fast4g ${fast.fetchedAt} / none ${none.fetchedAt}  
**Lighthouse:** ${slow.lighthouseVersion}

## 프리셋

| 프로필 | 네트워크 스로틀 | 비고 |
|--------|-----------------|------|
| Slow 4G | devtools DL ${slow.throttle.downloadThroughputKbps} / UL ${slow.throttle.uploadThroughputKbps} / RTT ${slow.throttle.requestLatencyMs}ms / CPU ${slow.throttle.cpuSlowdownMultiplier}× | 모바일 화면 에뮬 |
| Fast 4G | devtools DL ${fast.throttle.downloadThroughputKbps} / UL ${fast.throttle.uploadThroughputKbps} / RTT ${fast.throttle.requestLatencyMs}ms / CPU ${fast.throttle.cpuSlowdownMultiplier}× | 모바일 화면 에뮬 |
| No throttling | \`--throttling-method=provided\` + \`--screenEmulation.disabled\` + \`--no-emulated-user-agent\` | Lighthouse 문서형 “에뮬/스로틀 끔” |

## 측정 결과 (Performance)

| 프로필 | Perf | FCP (ms) | LCP (ms) | SI (ms) | TBT (ms) | **TTI (ms)** |
|--------|-----:|---------:|---------:|--------:|---------:|-------------:|
| Slow 4G | ${slow.performanceScore ?? "—"} | ${slow.fcpMs ?? "—"} | ${slow.lcpMs ?? "—"} | ${slow.siMs ?? "—"} | ${slow.tbtMs ?? "—"} | **${slow.ttiMs ?? "—"}** |
| Fast 4G | ${fast.performanceScore ?? "—"} | ${fast.fcpMs ?? "—"} | ${fast.lcpMs ?? "—"} | ${fast.siMs ?? "—"} | ${fast.tbtMs ?? "—"} | **${fast.ttiMs ?? "—"}** |
| No throttling | ${none.performanceScore ?? "—"} | ${none.fcpMs ?? "—"} | ${none.lcpMs ?? "—"} | ${none.siMs ?? "—"} | ${none.tbtMs ?? "—"} | **${none.ttiMs ?? "—"}** |

원시 JSON: \`latest-slow4g.json\`, \`latest-fast4g.json\`, \`latest-none.json\`
`;

await writeFile(path.join(outDir, "baseline.md"), md, "utf8");
console.error("Wrote docs/lighthouse/network-profiles/baseline.md");
